Value stringAtSafe(VMCtx *vmCtx, ObjString *str, int32_t index) {
	int32_t realIndex = (index < 0) ? str->string.length + index : index;

	if (ELOX_UNLIKELY((realIndex < 0) || (realIndex > str->string.length - 1)))
		return runtimeError(vmCtx, "String index out of range");

	return OBJ_VAL(copyString(vmCtx, str->string.chars + realIndex, 1));
}

Value stringStartsWith(Args *args) {
	ObjString *inst = AS_STRING(getValueArg(args, 0));
	ObjString *prefix;
	ELOX_GET_STRING_ARG_ELSE_RET(&prefix, args, 1);

	const uint8_t *strChars = inst->string.chars;
	const uint8_t *prefixChars = prefix->string.chars;

	if ((!strChars) || (!prefixChars))
		return BOOL_VAL(false);

	int strLen = inst->string.length;
	int prefixLen = prefix->string.length;
	if (prefixLen > strLen)
		return BOOL_VAL(false);
	return BOOL_VAL(memcmp(strChars, prefixChars, prefixLen) == 0);
}

Value stringEndsWith(Args *args) {
	ObjString *inst = AS_STRING(getValueArg(args, 0));
	ObjString *suffix;
	ELOX_GET_STRING_ARG_ELSE_RET(&suffix, args, 1);

	const uint8_t *strChars = inst->string.chars;
	const uint8_t *suffixChars = suffix->string.chars;

	if ((!strChars) || (!suffixChars))
		return BOOL_VAL(false);
	int strLen = inst->string.length;
	int suffixLen = suffix->string.length;
	if (suffixLen > strLen)
		return BOOL_VAL(false);
	return BOOL_VAL(memcmp(strChars + (strLen -suffixLen), suffixChars, suffixLen) == 0);
}

